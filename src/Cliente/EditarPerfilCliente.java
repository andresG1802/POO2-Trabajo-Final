    private void editarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editarButtonActionPerformed
        String nombres = nombresField.getText();
        String apellidos = apellidosField.getText();
        String correo = correoField.getText();
        cliente.setNombres(nombres);
        cliente.setApellidos(apellidos);
        cliente.setCorreo(correo);
        PreparedStatement st;
        ResultSet rs;
            try {
                cn.con.setAutoCommit(false);
                st = cn.con.prepareStatement("update cliente set nombre_cliente = ?,"
                        + "apellido_cliente = ?, correo_cliente = ? where dni_cliente = ?");
                st.setString(1,nombres);
                st.setString(2,apellidos);
                st.setString(3,correo);
                st.setString(4,cliente.getDni());
                st.executeUpdate();
                cn.con.commit();
            } catch (Exception e) {
                System.out.println(e);
            } 
            JOptionPane.showMessageDialog(null,"Cambios realizados"); 
            setVisible(false);
                    InicioCliente ventanaInicioCliente = new InicioCliente();
        ventanaInicioCliente.setVisible(true);


    }//GEN-LAST:event_editarButtonActionPerformed

